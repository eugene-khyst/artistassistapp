#!/usr/bin/env node

/**
 * Generates a 64×64 blue noise threshold texture using the void-and-cluster algorithm.
 * Output: src/services/image/filter/blue-noise.ts
 */

import {writeFileSync} from 'node:fs';
import {dirname, join} from 'node:path';
import {fileURLToPath} from 'node:url';

const SIZE = 64;
const N = SIZE * SIZE;
const SIGMA = 1.5;
const KERNEL_RADIUS = Math.ceil(2.5 * SIGMA);
const KERNEL_SIZE = 2 * KERNEL_RADIUS + 1;
const INITIAL_DENSITY = 0.1;
const SEED = 42;

// --- Seeded RNG (mulberry32) ---

function createRng(seed) {
  return () => {
    seed |= 0;
    seed = (seed + 0x6d2b79f5) | 0;
    let t = Math.imul(seed ^ (seed >>> 15), 1 | seed);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

// --- Precompute Gaussian kernel ---

const kernel = new Float64Array(KERNEL_SIZE * KERNEL_SIZE);
const invTwoSigmaSq = 1 / (2 * SIGMA * SIGMA);
for (let dy = -KERNEL_RADIUS; dy <= KERNEL_RADIUS; dy++) {
  for (let dx = -KERNEL_RADIUS; dx <= KERNEL_RADIUS; dx++) {
    kernel[(dy + KERNEL_RADIUS) * KERNEL_SIZE + (dx + KERNEL_RADIUS)] = Math.exp(
      -(dx * dx + dy * dy) * invTwoSigmaSq
    );
  }
}

// --- Energy tracking with incremental updates ---

const energy = new Float64Array(N);
const seeds = new Uint8Array(N);

function updateEnergy(pos, sign) {
  const sy = (pos / SIZE) | 0;
  const sx = pos % SIZE;
  for (let dy = -KERNEL_RADIUS; dy <= KERNEL_RADIUS; dy++) {
    const ny = (((sy + dy) % SIZE) + SIZE) % SIZE;
    for (let dx = -KERNEL_RADIUS; dx <= KERNEL_RADIUS; dx++) {
      const nx = (((sx + dx) % SIZE) + SIZE) % SIZE;
      energy[ny * SIZE + nx] +=
        sign * kernel[(dy + KERNEL_RADIUS) * KERNEL_SIZE + (dx + KERNEL_RADIUS)];
    }
  }
}

function addSeed(pos) {
  seeds[pos] = 1;
  updateEnergy(pos, 1);
}

function removeSeed(pos) {
  seeds[pos] = 0;
  updateEnergy(pos, -1);
}

function findTightestCluster() {
  let max = -Infinity;
  let idx = 0;
  for (let i = 0; i < N; i++) {
    if (seeds[i] && energy[i] > max) {
      max = energy[i];
      idx = i;
    }
  }
  return idx;
}

function findLargestVoid() {
  let min = Infinity;
  let idx = 0;
  for (let i = 0; i < N; i++) {
    if (!seeds[i] && energy[i] < min) {
      min = energy[i];
      idx = i;
    }
  }
  return idx;
}

// --- Void-and-cluster algorithm ---

const rng = createRng(SEED);
const rank = new Uint16Array(N);

// Initial binary pattern (~10% density)
const initialPositions = [];
for (let i = 0; i < N; i++) {
  if (rng() < INITIAL_DENSITY) {
    initialPositions.push(i);
  }
}
for (const pos of initialPositions) {
  addSeed(pos);
}

// Phase 1: Improve initial pattern
for (;;) {
  const tightest = findTightestCluster();
  removeSeed(tightest);
  const voidPos = findLargestVoid();
  addSeed(voidPos);
  if (tightest === voidPos) break;
}

let seedCount = 0;
for (let i = 0; i < N; i++) {
  if (seeds[i]) seedCount++;
}

// Phase 2: Rank by progressive removal (high to low)
for (let r = seedCount - 1; r >= 0; r--) {
  const tightest = findTightestCluster();
  removeSeed(tightest);
  rank[tightest] = r;
}

// Phase 3: Rank by progressive insertion (low to high)
for (let r = seedCount; r < N; r++) {
  const voidPos = findLargestVoid();
  addSeed(voidPos);
  rank[voidPos] = r;
}

// --- Write TypeScript module ---

const bytes = new Uint8Array(N);
for (let i = 0; i < N; i++) {
  bytes[i] = Math.round((rank[i] / (N - 1)) * 255);
}
const base64 = Buffer.from(bytes).toString('base64');

const outPath = join(
  dirname(fileURLToPath(import.meta.url)),
  'src/services/image/filter/blue-noise.ts'
);

writeFileSync(
  outPath,
  `/* eslint-disable */
// Generated by generate-blue-noise.mjs — do not edit manually

export const BLUE_NOISE_SIZE = ${SIZE};

// prettier-ignore
const BLUE_NOISE_BASE64 = '${base64}';

function decodeBase64ToUint8(base64: string): Uint8Array {
  const binaryString = atob(base64);
  const bytes = new Uint8Array(binaryString.length);
  for (let i = 0; i < binaryString.length; i++) {
    bytes[i] = binaryString.charCodeAt(i);
  }
  return bytes;
}

export const BLUE_NOISE = decodeBase64ToUint8(BLUE_NOISE_BASE64);
`
);
console.log(`Blue noise texture written to ${outPath}`);
